int sumar(int num1, int num2){
	int resultado;

	resultado = num1 + num2;

	return resultado;
}

int resta(int num1, int num2){
	int resultado;

	resultado = num1 - num2;

	return resultado;
}

int multiplicar(int num1, int num2){
	int resultado;

	resultado = num1 * num2;

	return resultado;
}

int divicion(int num1, int num2){
	float resultado;

	resultado = num1 / num2;

	return resultado;
}

